package com.echo.web.util.common;


public class SpellOption {
	private static StringBuffer result = new StringBuffer("");
	public static StringBuffer spell(String A,String B,String C,String D){
		result.append(A+"@@"+B+"@@"+C+"@@"+D);
		return result;
	}
	public static StringBuffer spellPhoto(String photo1,String photo2){
		result.append(photo1+"@@"+photo2);
		return result;
	}
}
